#include <stdio.h>

int main() {
    int a, b, c;

    printf("Введите три целых числа(через пробел): ");
    scanf("%d %d %d", &a, &b, &c);

    /*
    алгоритм определения произведения знаков чисел:
    
    1. для каждого числа вычисляется его знак:
       - (a > 0) - (a < 0) даёт:
         *  1 - 0 =  1  если число положительное (a > 0)
         *  0 - 1 = -1  если число отрицательное (a < 0)
         *  0 - 0 =  0  если число равно нулю (a == 0)
    
    2. знаки перемножаются, что даёт:
       -  1: если четное количество отрицательных чисел (включая 0)
       - -1: если нечетное количество отрицательных чисел
       -  0: если хотя бы одно число равно нулю
     */
    
    int sign = (a > 0) - (a < 0);  // определяем знак первого числа
    sign *= (b > 0) - (b < 0);     // умножаем на знак второго числа
    sign *= (c > 0) - (c < 0);     // умножаем на знак третьего числа
    
    /*
    Примеры:
    2, 5, 10 ->  1 *  1 *  1 =  1
    3, 4, -2 ->  1 *  1 * -1 = -1
    -5, 0, 3 -> -1 *  0 *  1 =  0
    -2,-4, 3 -> -1 * -1 *  1 =  1
    */
    
    // вывод результата произведения знаков
    printf("%d\n", sign);

    return 0;
}
